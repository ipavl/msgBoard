<h2>Frequently Asked Questions</h2>

<h3>What is msgBoard?</h3>

<p>msgBoard is a simplistic online forum/message board (textboard) software that can be used for holding discussions that don't require 
	much in terms of functionality of anything but text (and a few other features thrown in for kicks) - think of it like a large 
	collection of public group emails.</p>

<h3>What is and is not allowed?</h3>

<p>To keep things simple, nothing illegal is allowed, and please try and be polite and respectful to each other. This is the internet, 
	so whatever you post here could potentially be seen by anyone.</p>

<h3>What does it mean if a user is "Verified" when viewing a thread?</h3>

<p>It means the user has <a href="./?register">registered their name</a> and has posted with the correct credentials. It is how you can 
	tell that someone is who they say they are.</p>
	
<h3>I've forgotten my password. What can I do?</h3>

<p>Make a thread and we'll manually reset it for you. An automated form will eventually exist.</p>